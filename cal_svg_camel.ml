let make_month t year month =
  let empty_day = 0 in
  let m = Array.make_matrix 6 7 empty_day in
  let ofs = ref 0 in
  for day = 1 to 31 do
    let tm =
      { t with
        Unix.tm_year = year - 1900;
        Unix.tm_mon = month;
        Unix.tm_mday = day;
      }
    in
    (* with Unix.mktime '40 October' is changed into '9 November' *)
    let _, this = Unix.mktime tm in
    if !ofs = 0 then ofs := (this.Unix.tm_wday + off) mod 7;
    if t_same this tm then
      let i, j = indices !ofs in
      m.(i).(j) <- day;
    incr ofs;
  done;
  (m)


let current_year () =
  let t = Unix.localtime (Unix.time ()) in
  (t.Unix.tm_year  + 1900)


let () =
  let year =
    try int_of_string Sys.argv.(1)
    with _ -> current_year ()
  in

  let svg = new_svg_document ~width:210 ~height:297 () in

  (* CSS styles *)
  let css = new_css () in
  css_add css ~selector:".month_name" ~style:[
    text_anchor, "middle";
    font_family, "sans-serif";
    font_size, "14.8";
    font_weight, "bold";
    fill, "#444";
    fill_opacity, "0.8";
  ];
  css_add css ~selector:".day_name" ~style:[
    text_anchor, "middle";
    font_family, "sans-serif";
    font_size, "9.0";
    font_weight, "normal";
    fill, "#000";
  ];
  css_add css ~selector:".day_num" ~style:[
    text_anchor, "middle";
    font_family, "sans-serif";
    font_size, "14.2";
    font_weight, "normal";
    fill, "#222";
  ];
  css_add css ~selector:".sep_line" ~style:[
    stroke, "#FFF";
    stroke_width, "1.0";
    stroke_opacity, "0.9";
  ];
  add_css_to_svg css svg;

  add_blur_filter svg ~id:"filter1" ~stdDeviation:0.227 ();

  (* Background *)
  let bg_color = "#FFF" in
  let fill = bg_color in
  add_rect svg ~x:0.0 ~y:0.0 ~width:210.0 ~height:297.0 ~fill ();
  add_newline svg;

  add_camel svg ();
  add_newline svg;

  let text_anchor = "left" in
  let font_family = "sans-serif" in
  let font_weight = "bold" in

  (* Year and title *)
  let text = Printf.sprintf "%s %d" (String.capitalize_ascii cal) year in
  add_text svg ~x:13.0 ~y:70.9 ~text_anchor ~font_family ~font_size:9.4 ~font_weight ~fill:"#000" ~fill_opacity:0.6 ~filter:"url(#filter1)" ~text ();
  add_text svg ~x:13.8 ~y:70.2 ~text_anchor ~font_family ~font_size:9.4 ~font_weight ~fill:"#FFF" ~text ();
  add_newline svg;

  (* OCaml title *)
  let text = "OCaml" in
  add_text svg ~x:140.0 ~y:18.9 ~text_anchor ~font_family ~font_size:14.0 ~font_weight ~fill:"#000" ~fill_opacity:0.6 ~filter:"url(#filter1)" ~text ();
  add_text svg ~x:140.9 ~y:18.1 ~text_anchor ~font_family ~font_size:14.0 ~font_weight ~fill:"#FFF" ~text ();
  add_newline svg;

  (* For each month *)
  for mon = 1 to 12 do

    let tx, ty =
      let m = pred mon in
      float (12 + (m mod 3) * 64),
      float (78 + (m / 3) * 53)
    in

    begin_group svg ~translate:(tx, ty) ~scale:(0.26, 0.26);
    add_newline svg;

    let cell_height = 23 in
    let days_h_spacing = 31 in

    (* Month block background *)
    let height = 24 + 16 + 6 * cell_height + 12 in
    let height = float height in
    let fill = "#f48a32" in
    add_rect svg ~x:0.0 ~y:6.0 ~width:225.0 ~height ~rx:14.0 ~ry:14.0 ~fill_opacity:0.7 ~fill ();

    (* Month label *)
    let text = String.capitalize_ascii months.(pred mon) in
    add_text svg ~x:110.0 ~y:26.0 ~css:"month_name" ~text ();
    add_newline svg;

    (* Labels: days names *)
    for i = 0 to 6 do
      let x = float (19 + i * days_h_spacing) in
      let text = days_abbr.(days_order.(i)) in
      add_text svg ~x ~y:44.0 ~css:"day_name" ~text ();
    done;
    add_newline svg;

    let y = 50 in
    add_line svg ~x1:6 ~y1:y ~x2:(days_h_spacing * 7 + 2) ~y2:y ~css:"sep_line" ();
    add_newline svg;

    let t = Unix.gmtime 0.0 in
    let m = make_month t year (pred mon) in

    let len = Array.length m in
    let num_rows =
      if m.(len-1).(0) = 0
      then (len-1)
      else (len)
    in

    (* Day Numbers *)
    for w = 0 to pred num_rows do  (* for each row of the month *)
      for i = 0 to 6 do  (* for each day *)
        let x = 4 + i * days_h_spacing in
        let y = 50 + w * cell_height in
        let d = m.(w).(i) in
        if d = 0 then begin
          if w = 0 then ()
        end else begin
          let text = Printf.sprintf "%d" d in
          let x, y = float (x+15), float (y+18) in
          add_text svg ~x ~y ~css:"day_num" ~text ();
        end;
      done;
      add_newline svg;
    done;
    end_group svg;
  done;
  add_newline svg;
  finish_svg svg;
  print_string (get_svg_document svg);
